/**
 * Kill the orchestrator tmux session.
 * Returns true if killed, false if session didn't exist.
 */
export function killOrchestratorSession(): boolean {
  const session = resolveSession('orchestrator')!;

  try {
    execFileSync(TMUX_BIN, ['-S', TMUX_SOCKET, 'kill-session', '-t', `=${session}`], {
      timeout: 5000,
    });
    log.info('Orchestrator session killed', { session });
    return true;
  } catch {
    log.warn('Orchestrator session not found for kill', { session });
    return false;
  }
}

/**
 * Check if the orchestrator session exists (alive = session running).
 * This returns true even when the wrapper is idle-waiting between tasks.
 */
export function isOrchestratorAlive(): boolean {
  const session = resolveSession('orchestrator')!;

  try {
    execFileSync(TMUX_BIN, ['-S', TMUX_SOCKET, 'has-session', '-t', `=${session}`], {
      timeout: 5000,
    });
    return true;
  } catch {
    return false;
  }
}

/**
 * Check if the orchestrator is actively running Claude (not just idle-waiting).
 *
 * The wrapper script runs Claude as a child of bash, so tmux's
 * pane_current_command always reports "bash". Instead, we grab the wrapper's
 * PID (#{pane_pid}) and use pgrep to check whether any "claude" process is a
 * descendant of that PID.
 *
 * Returns:
 *  - 'active'  — session exists and a claude process is running under the wrapper
 *  - 'waiting' — session exists but no claude child (idle polling loop)
 *  - 'dead'    — no session
 */
export function getOrchestratorState(): 'active' | 'waiting' | 'dead' {
  const session = resolveSession('orchestrator')!;

  try {
    execFileSync(TMUX_BIN, ['-S', TMUX_SOCKET, 'has-session', '-t', `=${session}`], {
      timeout: 5000,
    });
  } catch {
    return 'dead';
  }

  try {
    // Get the PID of the wrapper bash process that owns the tmux pane
    const panePid = execFileSync(TMUX_BIN, [
      '-S', TMUX_SOCKET,
      'display-message',
      '-t', `${session}:`,
      '-p', '#{pane_pid}',
    ], { timeout: 5000, encoding: 'utf8' }).trim();

    if (!panePid || !/^\d+$/.test(panePid)) {
      return 'waiting';
    }

    // Check if any "claude" process is a descendant of the wrapper PID
    try {
      execFileSync('/usr/bin/pgrep', ['-P', panePid, '-f', 'claude'], {
        timeout: 5000,
      });
      // pgrep exits 0 → at least one claude descendant found
      return 'active';
    } catch {
      // pgrep exits non-zero → no matching descendants
      return 'waiting';
    }
  } catch (err) {
    log.warn('getOrchestratorState: failed to determine process tree, assuming waiting', {
      error: err instanceof Error ? err.message : String(err),
    });
    return 'waiting';
  }
}

/**
 * List all tmux sessions managed by the daemon.
 */
export function listSessions(): string[] {
  try {
    const output = execFileSync(TMUX_BIN, [
      '-S', TMUX_SOCKET,
      'list-sessions',
      '-F', '#{session_name}',
    ], { timeout: 5000 }).toString().trim();
    return output ? output.split('\n') : [];
  } catch {
    return [];
  }
}

